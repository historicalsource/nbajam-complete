	mpyu	a2,a7
;	addk	3,a7			;Round up to 4 bit boundary
;	srl	2,a7
;	sll	5,a7			;X size*8

	addi	>10000,a2		;Y Size 1
	move	*a1+,a8			;Get Y size

	ori	DMACNZ,a5
	move	a9,a1
fdlp	calla	QDMAN
	addi	>1010000,a1		;Next color
	add	a7,a4			;Set SAG for next line
	addi	>10000,a3		;Y+1
	dsj	a8,fdlp

	rets


