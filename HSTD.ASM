**************************************************************************
*
*	FIND_TABLE_LEVEL
*
*	THIS IS CALLED TO COMPARE THE SCORE IN A0 TO
*	THE TABLE POINTED TO BY A8.
*
*	RETURN PHYSICALLY WHERE IT FITS IN A0, AND
